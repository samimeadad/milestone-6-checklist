/*
1. map() will loop thorugh each element of an array and do some operation on them and return the value as an array.
2. forEach() will do the same as map(), loop through each element of an array and do some operation on them, but does not return any value from the function.
3. filter() will loop through each element of an array and return an array consisting of the elements, which satisfy one or more conditions set in the function.
4. find() will loop through the array and return the item first met the condition set in the funciton.
*/